#include "wolf3d.h"
#include "draw_map.h"

#define CELL 50

#define ROWS 24
#define COLS 24

#define WIDTH (ROWS * CELL)
#define HEIGHT (COLS * CELL)

int	motion_hook(int x, int y, void *p)
{
	t_env	*env = (t_env*)p;

	env->hero->pos.x = x;
	env->hero->pos.y = y;

	return (0);
}

